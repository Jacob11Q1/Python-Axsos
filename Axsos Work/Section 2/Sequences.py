# Sequences:

my_list = [99,4,2,5,-3]
my_tuple = (99,4,2,5,-3)
my_str = "sequoia"
print(my_list[:]) # Output: [99, 4, 2, 5, -3]
print(my_tuple[1:]) # Output: (4, 2, 5, -3) / it started from 4
print(my_str[:3]) # Output: seq
print(my_tuple[2:4]) # Output: (2, 5)
