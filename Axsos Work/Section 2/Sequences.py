# Examples: 

# 1) max(sequence): Returns the largest value in the sequence
# 2) sum(sequence): Returns the sum of all values in the sequence
# 3) map(function , sequence): Applies the function to every item in the sequence you pass in. Returns a list of the results.
# 4) min(sequence): Returns the lowest value in a sequence.
# 5) sorted(sequence): Returns a sorted list of the sequence's values

